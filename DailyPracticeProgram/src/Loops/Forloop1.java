package Loops;

public class Forloop1 {

	public static void main(String[] args) {
		for(int i =1;i<6;i++) {
			System.out.println("Nazmin shaikh");
	}
		System.out.println("---------------------------------");
		for(int i=10;i>6;i--) {
			System.out.println("Nazmin shaikh");
			
	}
		System.out.println("---------------------------------");
		for(int i=1;i<=6;i++) {
			System.out.print(i);
			System.out.println();
		}
		System.out.println("---------------------------------");
		for(int i=10;i>6;i--) {
			System.out.print(i);
	}

	}
}